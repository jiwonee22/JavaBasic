package java06_class.field;

public class MemberField_01 {

	//멤버 필드
	private int num = 999;
	private String name = "Alice";
	
	public int getNum() {
		return num;
	}
	
	public void setNum(int num) {
		this.num = num;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	
	
	
	// Source메뉴를 팝업으로 띄우는 단축키 : alt + shift + s
	
	// Getter, Setter 자동생성 단축키 : alt + shift + s , r
	
}
