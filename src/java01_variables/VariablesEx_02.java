package java01_variables;

public class VariablesEx_02 {

	public static void main(String[] args) {

		int num; //int형 변수 선언
		// -> int형 데이터를 저장할 수 있는 메모리 공간 생성
		
		num = 13; //num변수 공간에 13값을 대입(assign)
		// -> num변수 공간에 데이터를 저장한다
		
		num = 78; //num변수 공간에 78 값을 대입
		// -> 여러개 저장 불가, 13이 있던 자리에 78을 덧씌움
		
		System.out.println(num);
		// -> num변수에 저장된 값을 불러와서 출력한다
		
	}

}
