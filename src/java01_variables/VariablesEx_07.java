package java01_variables;

public class VariablesEx_07 {

	public static void main(String[] args) {
	
		//자바의 자료형(Data Type)
		//	8가지 기본데이터타입(Primitive Type)
		//	참조형 데이터타입(Reference Type) - 클래스(객체지향)
		
		//---------------------------------------------------------------------
		
		//String 타입
		//	문자열
		//	참조형 데이터타입 중 하나
		
		//	""큰따옴표로 여러 개의 문자를 감싸서 표현한다
		// 	-> String형 리터럴 상수
		
		String fruit; //문자열 변수 선언
		fruit = "Apple"; //문자열상수 "Apple" 값을 fruit변수에 대입
		System.out.println(fruit); //문자열 변수 fruit 출력
		
		fruit = "Banana";
		System.out.println(fruit);
		
	}
	
}
